public class Literals4{

    public static void main(String a[]){
        /*
            Literals : 
            0x7E(hexadecimal) - 126
            10000000 - 10_00_00_00 
            12e10 = 12 x 10^10;
        */

       int num1 = 10_00_00_00;
       double num2 = 45;
       System.out.println(num1);
       System.out.println(num2);
        
    }
}